package com.dean.bean;

public class ExamResult {
 
	private int examarr_id;
	private int s_score;
	private int m_score;
	private int p_score;
	private int j_score;
	private int total_score;
	private int examres_id;
	private String stu_number;
	public int getExamarr_id() {
		return examarr_id;
	}
	public void setExamarr_id(int examarr_id) {
		this.examarr_id = examarr_id;
	}
	public int getS_score() {
		return s_score;
	}
	public void setS_score(int s_score) {
		this.s_score = s_score;
	}
	public int getM_score() {
		return m_score;
	}
	public void setM_score(int m_score) {
		this.m_score = m_score;
	}
	public int getP_score() {
		return p_score;
	}
	public void setP_score(int p_score) {
		this.p_score = p_score;
	}
	public int getJ_score() {
		return j_score;
	}
	public void setJ_score(int j_score) {
		this.j_score = j_score;
	}
	public int getTotal_score() {
		return total_score;
	}
	public void setTotal_score(int total_score) {
		this.total_score = total_score;
	}
	public int getExamres_id() {
		return examres_id;
	}
	public void setExamres_id(int examres_id) {
		this.examres_id = examres_id;
	}
	public String getStu_number() {
		return stu_number;
	}
	public void setStu_number(String stu_number) {
		this.stu_number = stu_number;
	}
	
}
