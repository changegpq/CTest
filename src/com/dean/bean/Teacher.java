public class Teacher {
	private String tea_number;
	private String tea_name;
	private String tea_pwd;
	private String tea_email;
	private String tea_last_time;
	public String getTea_number() {
		return tea_number;
	}
	public void setTea_number(String tea_number) {
		this.tea_number = tea_number;
	}
	public String getTea_name() {
		return tea_name;
	}
	public void setTea_name(String tea_name) {
		this.tea_name = tea_name;
	}
	public String getTea_pwd() {
		return tea_pwd;
	}
	public void setTea_pwd(String tea_pwd) {
		this.tea_pwd = tea_pwd;
	}
	public String getTea_email() {
		return tea_email;
	}
	public void setTea_email(String tea_email) {
		this.tea_email = tea_email;
	}
	public String getTea_last_time() {
		return tea_last_time;
	}
	public void setTea_last_time(String tea_last_time) {
		this.tea_last_time = tea_last_time;
	}
}
